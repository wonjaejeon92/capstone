package e.t.gameworkshop;

public class PrevIndexRobot {

    private int row;
    private int col;
    private int index;

    public PrevIndexRobot() {
        row = 0;
        col = 0;
        index = 0;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
